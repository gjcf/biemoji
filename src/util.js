export function string2emoji(words) {
  const keys = words.match(/\[(.+?)\]/g) || []
  let result = words
  for (const key of keys) {
    if (wxEmojiArray[key]) {
      result = result.replace(new RegExp(`\\[${key.slice(1, -1)}\\]`, 'g'), `<img src="https://unpkg.com/biemoji@latest/dist/wx/${wxEmojiArray[key]}.png" alt="${wxEmojiArray[key]}" class="emoji">`)
    }
    if (qqEmojiArray[key]) {
      result = result.replace(new RegExp(`\\[${key.slice(1, -1)}\\]`, 'g'), `<img src="https://unpkg.com/biemoji@latest/dist/qq/${qqEmojiArray[key]}.gif" alt="${qqEmojiArray[key]}" class="emoji">`)
    }
    if (kaEmojiArray[key]) {
      result = result.replace(new RegExp(`\\[${key.slice(1, -1)}\\]`, 'g'), `<img src="https://unpkg.com/biemoji@latest/dist/ka/${kaEmojiArray[key]}.png" alt="${kaEmojiArray[key]}" class="emoji">`)
    }
  }
  return result
}

export function deleteEmoji(text) {
  const reg = /\[[^\]]+?\]$/
  if (reg.test(text)) {
    return text.replace(reg, '')
  } else if (text) {
    return text.slice(0, -1)
  }
}